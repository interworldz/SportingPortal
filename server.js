app.get('/api/news/:id', (req, res) => {
  const id = Number.parseInt(req.params.id, 10);

  if (!Number.isInteger(id) || id < 0 || id >= news.length) {
    return res.status(404).json({ error: 'Новину не знайдено' });
  }

  return res.json(news[id]);
});

